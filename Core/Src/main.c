#define TIMER_PERIOD 0.000005234375
#define MIN_FREQ 2.9
#define MAX_FREQ 66.1
uint8_t setFrequency(uint8_t port, float nFreq){
	if((port<0)||(port>5)){
		return 0;
	}else if((nFreq<MIN_FREQ)||(nFreq>MAX_FREQ)){
		return 0;
	}
	float nPeriod = 1.0/nFreq;  //Period in seconds
	uint32_t nARR = nPeriod/TIMER_PERIOD;   //Calculates the timer reload counter
	uint32_t nCCR = nARR/2;				   //Calculates the counter compare to get a square wave
	switch(port){
	case 1:
		htim1.Instance->ARR = nARR;
		htim1.Instance->CCR1 = nCCR;
		break;
	case 2:
		htim2.Instance->ARR = nARR;
		htim2.Instance->CCR1 = nCCR;
		break;
	case 3:
		htim3.Instance->ARR = nARR;
		htim3.Instance->CCR1 = nCCR;
		break;
	case 4:
		htim4.Instance->ARR = nARR;
		htim4.Instance->CCR1 = nCCR;
		break;
	default:
		return 0;
	}
	return 1;
}
uint8_t setEnable(uint8_t port, uint8_t nState){
	if((port<0)||(port>5)){
		return 0;
	}else if((nState!=1)&&(nState!=0)){
		return 0;
	}

	switch(port){
	case 1:
		HAL_GPIO_WritePin(EN_1_GPIO_Port, EN_1_Pin, nState);
		break;
	case 2:
		HAL_GPIO_WritePin(EN_2_GPIO_Port, EN_2_Pin, nState);
		break;
	case 3:
		HAL_GPIO_WritePin(EN_3_GPIO_Port, EN_3_Pin, nState);
		break;
	case 4:
		HAL_GPIO_WritePin(EN_4_GPIO_Port, EN_4_Pin, nState);
		break;
	default:
		return 0;
	}
	return 1;
}
